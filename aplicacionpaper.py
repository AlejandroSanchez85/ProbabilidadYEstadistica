    def coefficient_table(self):
        """
        Tabla resumida de coeficientes para explicar la dirección del efecto.
        """
        if self.poisson_result is None or self.logit_result is None:
            raise RuntimeError("Primero ajusta ambos modelos.")

        poisson_coef = pd.DataFrame({
            "Variable": self.poisson_result.params.index,
            "Coef_Poisson": self.poisson_result.params.values,
            "p_value_Poisson": self.poisson_result.pvalues.values
        })

        logit_coef = pd.DataFrame({
            "Variable": self.logit_result.params.index,
            "Coef_Logit": self.logit_result.params.values
        })

        coef_df = poisson_coef.merge(logit_coef, on="Variable", how="outer")
        self.show_table(coef_df.round(4), "Coeficientes de los modelos")

        return coef_df

