class DengueProbabilisticAnalysis:
    """
    Clase principal para el análisis probabilístico del dengue.

    Estructura matemática del modelo:
    ----------------------------------
    1) Dependencia entre variables mediante una idea tipo copula gaussiana:
       Z ~ N(0, Sigma)
       U = Phi(Z)
       donde Phi es la CDF normal estándar.

    2) Modelo Poisson para los casos:
       Y_t ~ Poisson(lambda_t)
       log(lambda_t) = beta_0 + beta_1 X_1 + ... + beta_k X_k

    3) Modelo logístico para brote:
       P(Brote_t = 1) = 1 / (1 + exp(-(gamma_0 + gamma_1 X_1 + ... + gamma_k X_k)))

    El objetivo es que el flujo se parezca a un trabajo real de probabilidad y estadística.
    """

    def __init__(self, seed: int = 42, n_months: int = 72):
        """
        Parámetros
        ----------
        seed : int
            Semilla para reproducibilidad.
        n_months : int
            Número de observaciones mensuales sintéticas.
        """
        self.seed = seed
        self.n_months = n_months
        self.rng = np.random.default_rng(seed)

        # Variables que usaremos en el modelo
        self.feature_cols = [
            "temperature",
            "rainfall",
            "humidity",
            "population_density",
            "water_access",
            "sanitation_index",
            "mosquito_index",
            "lag_cases",
        ]

        # Contenedores para los datos y modelos
        self.data = None
        self.train_df = None
        self.test_df = None

        self.poisson_result = None
        self.nb_result = None
        self.logit_result = None

        # Escalado hecho con el conjunto de entrenamiento
        self.scaler_means = {}
        self.scaler_stds = {}

        # Resultados de predicción
        self.poisson_test_pred = None
        self.logit_test_prob = None
        self.logit_test_pred = None

        # Generar datos sintéticos por defecto
        self.data = self._generate_synthetic_data()

    # ========================================================
    # 2. GENERACIÓN DE DATOS SINTÉTICOS
    # ========================================================

    def _generate_synthetic_data(self) -> pd.DataFrame:
        """
        Genera un conjunto de datos sintéticos mensuales con dependencia
        probabilística entre variables usando una lógica tipo copula gaussiana.

        Esta parte es útil porque, aunque no tengas datos reales todavía,
        te permite mostrar el procedimiento completo en clase.
        """

        # Fechas mensuales
        dates = pd.date_range(start="2019-01-01", periods=self.n_months, freq="MS")
        t = np.arange(self.n_months)

        # Componentes estacionales
        seasonal_sin = np.sin(2 * np.pi * t / 12)
        seasonal_cos = np.cos(2 * np.pi * t / 12)

        # Matriz de correlación para simular dependencia entre factores latentes
        # (idea tipo Gaussian Copula)
        corr = np.array([
            [1.0,  0.45, 0.35, 0.10, 0.20],
            [0.45, 1.0,  0.40, 0.10, 0.15],
            [0.35, 0.40, 1.0,  0.05, 0.10],
            [0.10, 0.10, 0.05, 1.0,  0.20],
            [0.20, 0.15, 0.10, 0.20, 1.0],
        ])

        # Variables latentes normales correlacionadas
        latent = self.rng.multivariate_normal(
            mean=np.zeros(5),
            cov=corr,
            size=self.n_months
        )

        # Transformación a uniformes [0,1]
        u = stats.norm.cdf(latent)
        u = np.clip(u, 1e-6, 1 - 1e-6)

        # Variables observadas
        temperature = 24.5 + 2.8 * seasonal_sin + 0.9 * latent[:, 0]
        rainfall = 70 + 55 * (1 + seasonal_cos) / 2 + stats.gamma.ppf(u[:, 1], a=2.3, scale=18)
        humidity = 78 + 5.5 * seasonal_sin + 2.5 * latent[:, 2]
        population_density = 1100 + 2.0 * t + 28 * latent[:, 3]
        water_access = 0.89 - 0.02 * (1 + seasonal_cos) / 2 + 0.015 * latent[:, 4]
        sanitation_index = 0.81 + 0.015 * seasonal_cos - 0.02 * np.abs(latent[:, 0])

        # Aseguramos límites realistas
        water_access = np.clip(water_access, 0.65, 0.98)
        sanitation_index = np.clip(sanitation_index, 0.60, 0.97)

        # Índice del vector (mosquito index)
        # Subirá con lluvia y humedad, bajará con mayor acceso a agua/saneamiento
        mosquito_linear = (
            -1.6
            + 0.015 * (rainfall - 100)
            + 0.060 * (humidity - 75)
            - 0.030 * (temperature - 24)
            + 2.3 * (1 - water_access)
            + 1.8 * (1 - sanitation_index)
        )

        mosquito_index = 1 / (1 + np.exp(-mosquito_linear))
        mosquito_index += 0.04 * self.rng.normal(size=self.n_months)
        mosquito_index = np.clip(mosquito_index, 0, 1)

        # Generación recursiva de casos con dependencia temporal
        # Y_t ~ Poisson(lambda_t)
        cases = np.zeros(self.n_months, dtype=int)
        cases[0] = int(self.rng.poisson(lam=25))

        for i in range(1, self.n_months):
            lag = cases[i - 1]

            # Estructura del riesgo: log(lambda_t)
            linear_risk = (
                1.20
                + 0.030 * (temperature[i] - 24)
                + 0.012 * ((rainfall[i] - 100) / 10)
                + 0.035 * (humidity[i] - 78)
                - 2.2 * (water_access[i] - 0.80)
                - 1.7 * (sanitation_index[i] - 0.75)
                + 1.9 * mosquito_index[i]
                + 0.0035 * lag
                + 0.00020 * (population_density[i] - 1100)
            )

            lam = np.exp(linear_risk)
            lam = max(lam, 1.0)
            cases[i] = int(self.rng.poisson(lam=lam))

        # Variable de brote: 1 si los casos están por encima del percentil 75
        threshold = np.quantile(cases, 0.75)
        outbreak = (cases >= threshold).astype(int)

        # Armamos el DataFrame
        df = pd.DataFrame({
            "date": dates,
            "temperature": np.round(temperature, 2),
            "rainfall": np.round(rainfall, 2),
            "humidity": np.round(humidity, 2),
            "population_density": np.round(population_density, 2),
            "water_access": np.round(water_access, 4),
            "sanitation_index": np.round(sanitation_index, 4),
            "mosquito_index": np.round(mosquito_index, 4),
            "cases": cases,
            "outbreak": outbreak,
        })

        # Variable rezagada
        df["lag_cases"] = df["cases"].shift(1)
        df.loc[0, "lag_cases"] = df.loc[0, "cases"]
        df["lag_cases"] = df["lag_cases"].astype(int)

        # Variables temporales auxiliares
        df["year"] = df["date"].dt.year
        df["month"] = df["date"].dt.month

        return df

    # ========================================================
    # 3. CARGA DE DATOS REALES
    # ========================================================

    def load_real_data(self, csv_path: str):
        """
        Carga un archivo CSV real con la misma estructura esperada.

        Columnas mínimas requeridas:
        - date
        - temperature
        - rainfall
        - humidity
        - population_density
        - water_access
        - sanitation_index
        - mosquito_index
        - cases

        Si el CSV no contiene outbreak ni lag_cases, el programa los construye.
        """

        df = pd.read_csv(csv_path)

        required_cols = {
            "date",
            "temperature",
            "rainfall",
            "humidity",
            "population_density",
            "water_access",
            "sanitation_index",
            "mosquito_index",
            "cases",
        }

        missing = required_cols - set(df.columns)
        if missing:
            raise ValueError(f"Faltan estas columnas obligatorias en el CSV: {missing}")

        df["date"] = pd.to_datetime(df["date"])
        df = df.sort_values("date").reset_index(drop=True)

        if "lag_cases" not in df.columns:
            df["lag_cases"] = df["cases"].shift(1)
            df.loc[0, "lag_cases"] = df.loc[0, "cases"]
            df["lag_cases"] = df["lag_cases"].fillna(df["cases"]).astype(int)

        if "outbreak" not in df.columns:
            threshold = np.quantile(df["cases"], 0.75)
            df["outbreak"] = (df["cases"] >= threshold).astype(int)

        df["year"] = df["date"].dt.year
        df["month"] = df["date"].dt.month

        self.data = df
        print("Datos reales cargados correctamente.")
        return self.data

