    def _split_train_test(self):
        """
        División temporal:
        - 80% entrenamiento
        - 20% prueba

        Se respeta el orden temporal para evitar fuga de información.
        """
        n_train = int(len(self.data) * 0.80)
        self.train_df = self.data.iloc[:n_train].copy()
        self.test_df = self.data.iloc[n_train:].copy()

    def _fit_scaler(self):
        """
        Calcula media y desviación estándar en entrenamiento
        para estandarizar variables.
        """
        for col in self.feature_cols:
            mean = self.train_df[col].mean()
            std = self.train_df[col].std()
            if std == 0:
                std = 1.0
            self.scaler_means[col] = mean
            self.scaler_stds[col] = std

    def _standardize_df(self, df: pd.DataFrame) -> pd.DataFrame:
        """
        Estandariza las variables usando la media y desviación del entrenamiento.
        """
        out = df.copy()
        for col in self.feature_cols:
            out[col] = (out[col] - self.scaler_means[col]) / self.scaler_stds[col]
        return out

    def _build_design_matrices(self):
        """
        Construye matrices X e y para Poisson y Logit.
        """
        self._split_train_test()
        self._fit_scaler()

        train_std = self._standardize_df(self.train_df)
        test_std = self._standardize_df(self.test_df)

        X_train = sm.add_constant(train_std[self.feature_cols], has_constant="add")
        X_test = sm.add_constant(test_std[self.feature_cols], has_constant="add")

        y_train_cases = self.train_df["cases"].astype(float)
        y_test_cases = self.test_df["cases"].astype(float)

        y_train_outbreak = self.train_df["outbreak"].astype(int)
        y_test_outbreak = self.test_df["outbreak"].astype(int)

        return X_train, X_test, y_train_cases, y_test_cases, y_train_outbreak, y_test_outbreak

    # ========================================================
    # 7. HERRAMIENTA SEGURA PARA EXTRAER EL PRIMER ESCALAR
    # ========================================================

    def _first_scalar(self, obj):
        """
        Extrae de forma segura el primer valor escalar de:
        - Series de pandas
        - DataFrames
        - arreglos numpy
        - listas

        Esto evita errores como KeyError: 0.
        """
        arr = np.asarray(obj).ravel()
        if arr.size == 0:
            raise ValueError("El objeto no contiene valores para extraer.")
        return float(arr[0])

    # ========================================================
    # 8. MODELO POISSON
    # ========================================================

    def fit_poisson_model(self):
        """
        Ajusta un modelo Poisson:

            log(lambda_t) = beta_0 + beta_1 x_1 + ... + beta_k x_k

        donde lambda_t es la media esperada de casos.

        También calcula una medida simple de sobre-dispersión.
        """
        X_train, X_test, y_train_cases, y_test_cases, _, _ = self._build_design_matrices()

        poisson_model = sm.GLM(
            y_train_cases,
            X_train,
            family=sm.families.Poisson()
        )
        self.poisson_result = poisson_model.fit()

        # Predicción en test
        self.poisson_test_pred = self.poisson_result.predict(X_test)

        # Diagnóstico simple de sobre-dispersión
        dispersion = self.poisson_result.pearson_chi2 / self.poisson_result.df_resid

        # Si la dispersión es alta, también ajustamos una versión binomial negativa
        if dispersion > 1.5:
            nb_model = sm.GLM(
                y_train_cases,
                X_train,
                family=sm.families.NegativeBinomial()
            )
            self.nb_result = nb_model.fit()
        else:
            self.nb_result = None

        print("\nMODELO POISSON")
        print("=" * 50)
        print(self.poisson_result.summary())
        print(f"\nÍndice de sobre-dispersión: {dispersion:.3f}")

        if self.nb_result is not None:
            print("\nMODELO BINOMIAL NEGATIVO (por sobre-dispersión)")
            print("=" * 50)
            print(self.nb_result.summary())

        return self.poisson_result

    def evaluate_poisson_model(self):
        """
        Evalúa el modelo Poisson en el conjunto de prueba.
        """
        _, X_test, _, y_test_cases, _, _ = self._build_design_matrices()
        y_pred = self.poisson_test_pred

        mae = mean_absolute_error(y_test_cases, y_pred)
        rmse = np.sqrt(mean_squared_error(y_test_cases, y_pred))
        r2 = r2_score(y_test_cases, y_pred)

        metrics_df = pd.DataFrame({
            "Métrica": ["MAE", "RMSE", "R2"],
            "Valor": [mae, rmse, r2]
        })

        self.show_table(metrics_df.round(4), "Métricas del modelo Poisson")

        pred_df = self.test_df[["date", "cases"]].copy()
        pred_df["predicted_cases"] = np.round(y_pred, 2)
        pred_df["error"] = np.round(pred_df["cases"] - pred_df["predicted_cases"], 2)

        self.show_table(pred_df.head(12), "Comparación casos reales vs predichos")

        return metrics_df, pred_df

