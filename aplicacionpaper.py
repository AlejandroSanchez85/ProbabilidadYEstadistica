    def _transform_single_row(self, row: pd.Series) -> pd.DataFrame:
        """
        Estandariza una sola fila usando estadísticas del entrenamiento.
        """
        x = row[self.feature_cols].copy()

        for col in self.feature_cols:
            x[col] = (x[col] - self.scaler_means[col]) / self.scaler_stds[col]

        x = pd.DataFrame([x])
        x = sm.add_constant(x, has_constant="add")
        return x

    def scenario_analysis(self):
        """
        Simula distintos escenarios para mostrar cómo cambian
        la cantidad esperada de casos y la probabilidad de brote.

        Esto es muy útil para conectar el análisis estadístico
        con decisiones de salud pública.
        """
        if self.poisson_result is None or self.logit_result is None:
            raise RuntimeError("Primero ajusta los modelos.")

        base = self.data.iloc[-1].copy()

        scenarios = {
            "Escenario base": base.copy(),
            "Mejor acceso a agua y saneamiento": base.copy(),
            "Control vectorial": base.copy(),
            "Escenario climático adverso": base.copy(),
        }

        # Escenario 2: mejora del acceso a agua y saneamiento
        scenarios["Mejor acceso a agua y saneamiento"]["water_access"] = min(base["water_access"] + 0.08, 0.99)
        scenarios["Mejor acceso a agua y saneamiento"]["sanitation_index"] = min(base["sanitation_index"] + 0.08, 0.99)
        scenarios["Mejor acceso a agua y saneamiento"]["mosquito_index"] = max(base["mosquito_index"] - 0.10, 0.01)

        # Escenario 3: control vectorial
        scenarios["Control vectorial"]["mosquito_index"] = max(base["mosquito_index"] - 0.20, 0.01)
        scenarios["Control vectorial"]["rainfall"] = max(base["rainfall"] - 8, 1.0)

        # Escenario 4: clima adverso
        scenarios["Escenario climático adverso"]["temperature"] = base["temperature"] + 1.5
        scenarios["Escenario climático adverso"]["rainfall"] = base["rainfall"] + 25
        scenarios["Escenario climático adverso"]["humidity"] = base["humidity"] + 6
        scenarios["Escenario climático adverso"]["mosquito_index"] = min(base["mosquito_index"] + 0.15, 0.99)

        rows = []
        for name, row in scenarios.items():
            x = self._transform_single_row(row)

            # Extracción segura del primer valor numérico.
            # Esto evita errores como KeyError: 0 cuando predict devuelve una Serie.
            pred_cases = self._first_scalar(self.poisson_result.predict(x))
            pred_prob = self._first_scalar(self.logit_result.predict(x))

            rows.append({
                "Escenario": name,
                "Casos_esperados": round(pred_cases, 2),
                "Probabilidad_brote": round(pred_prob, 4)
            })

        scenario_df = pd.DataFrame(rows)
        self.show_table(scenario_df, "Análisis de escenarios")

        # Gráfica de barras
        fig, ax = plt.subplots(figsize=(10, 5))
        ax.bar(scenario_df["Escenario"], scenario_df["Casos_esperados"])
        ax.set_title("Casos esperados por escenario")
        ax.set_ylabel("Casos esperados")
        ax.tick_params(axis="x", rotation=20)
        ax.grid(True, axis="y", alpha=0.3)
        plt.tight_layout()
        plt.show()

        fig, ax = plt.subplots(figsize=(10, 5))
        ax.bar(scenario_df["Escenario"], scenario_df["Probabilidad_brote"])
        ax.set_title("Probabilidad de brote por escenario")
        ax.set_ylabel("Probabilidad")
        ax.tick_params(axis="x", rotation=20)
        ax.grid(True, axis="y", alpha=0.3)
        plt.tight_layout()
        plt.show()

        return scenario_df

