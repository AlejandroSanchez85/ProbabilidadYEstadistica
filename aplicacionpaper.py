    # ========================================================
    # 9. MODELO LOGÍSTICO PARA BROTE
    # ========================================================

    def fit_logistic_model(self):
        """
        Ajusta un modelo logístico para brote:

            P(outbreak = 1) = 1 / (1 + exp(-(gamma_0 + gamma_1 x_1 + ...)))

        """
        X_train, X_test, _, _, y_train_outbreak, y_test_outbreak = self._build_design_matrices()

        try:
            logit_model = sm.Logit(y_train_outbreak, X_train)
            self.logit_result = logit_model.fit(disp=False, maxiter=200)
        except Exception:
            # Si hay problemas de convergencia, usamos regularización
            logit_model = sm.Logit(y_train_outbreak, X_train)
            self.logit_result = logit_model.fit_regularized(alpha=0.01, disp=False)

        self.logit_test_prob = self.logit_result.predict(X_test)
        self.logit_test_pred = (self.logit_test_prob >= 0.5).astype(int)

        print("\nMODELO LOGÍSTICO")
        print("=" * 50)
        print(self.logit_result.summary())

        return self.logit_result

    def evaluate_logistic_model(self):
        """
        Evalúa el modelo logístico con métricas de clasificación.
        """
        _, X_test, _, _, _, y_test_outbreak = self._build_design_matrices()

        y_prob = self.logit_test_prob
        y_pred = self.logit_test_pred

        acc = accuracy_score(y_test_outbreak, y_pred)
        prec = precision_score(y_test_outbreak, y_pred, zero_division=0)
        rec = recall_score(y_test_outbreak, y_pred, zero_division=0)
        f1 = f1_score(y_test_outbreak, y_pred, zero_division=0)

        try:
            auc = roc_auc_score(y_test_outbreak, y_prob)
        except Exception:
            auc = np.nan

        metrics_df = pd.DataFrame({
            "Métrica": ["Accuracy", "Precision", "Recall", "F1", "ROC AUC"],
            "Valor": [acc, prec, rec, f1, auc]
        })

        self.show_table(metrics_df.round(4), "Métricas del modelo logístico")

        cm = confusion_matrix(y_test_outbreak, y_pred)
        cm_df = pd.DataFrame(cm, index=["Real 0", "Real 1"], columns=["Pred 0", "Pred 1"])
        self.show_table(cm_df, "Matriz de confusión")

        return metrics_df, cm_df

    def plot_logistic_diagnostics(self):
        """
        Curva ROC y matriz de confusión en formato gráfico.
        """
        _, X_test, _, _, _, y_test_outbreak = self._build_design_matrices()

        # ROC
        try:
            fpr, tpr, _ = roc_curve(y_test_outbreak, self.logit_test_prob)
            fig, ax = plt.subplots(figsize=(7, 6))
            ax.plot(fpr, tpr, label="ROC")
            ax.plot([0, 1], [0, 1], "r--", label="Aleatorio")
            ax.set_title("Curva ROC del modelo logístico")
            ax.set_xlabel("False Positive Rate")
            ax.set_ylabel("True Positive Rate")
            ax.legend()
            ax.grid(True, alpha=0.3)
            plt.tight_layout()
            plt.show()
        except Exception:
            print("No fue posible construir la curva ROC con estos datos.")

        # Confusion matrix
        cm = confusion_matrix(y_test_outbreak, self.logit_test_pred)
        fig, ax = plt.subplots(figsize=(5, 5))
        im = ax.imshow(cm, cmap="Blues")
        ax.set_xticks([0, 1])
        ax.set_yticks([0, 1])
        ax.set_xticklabels(["Pred 0", "Pred 1"])
        ax.set_yticklabels(["Real 0", "Real 1"])
        ax.set_title("Matriz de confusión")
        for i in range(cm.shape[0]):
            for j in range(cm.shape[1]):
                ax.text(j, i, cm[i, j], ha="center", va="center", fontsize=12)
        fig.colorbar(im, ax=ax, shrink=0.8)
        plt.tight_layout()
        plt.show()

